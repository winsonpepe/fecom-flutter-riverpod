import '../order_screen/widgets/order_item_widget.dart';
import 'package:flutter/material.dart';
import 'package:pepe_s_application4/core/app_export.dart';
import 'package:pepe_s_application4/widgets/app_bar/appbar_image.dart';
import 'package:pepe_s_application4/widgets/app_bar/appbar_subtitle.dart';
import 'package:pepe_s_application4/widgets/app_bar/custom_app_bar.dart';

class OrderScreen extends StatelessWidget {
  const OrderScreen({Key? key}) : super(key: key);

  @override
  Widget build(BuildContext context) {
    mediaQueryData = MediaQuery.of(context);
    return SafeArea(
        child: Scaffold(
            appBar: CustomAppBar(
                leadingWidth: 40.h,
                leading: AppbarImage(
                    svgPath: ImageConstant.imgArrowleftBlueGray300,
                    margin:
                        EdgeInsets.only(left: 16.h, top: 15.v, bottom: 16.v),
                    onTap: () {
                      onTapArrowleftone(context);
                    }),
                title: AppbarSubtitle(
                    text: "lbl_order".tr, margin: EdgeInsets.only(left: 12.h))),
            body: Padding(
                padding: EdgeInsets.only(left: 15.h, top: 19.v, right: 15.h),
                child: ListView.separated(
                    physics: BouncingScrollPhysics(),
                    shrinkWrap: true,
                    separatorBuilder: (context, index) {
                      return SizedBox(height: 10.v);
                    },
                    itemCount: 3,
                    itemBuilder: (context, index) {
                      return OrderItemWidget(onTapOrder: () {
                        onTapOrder(context);
                      });
                    }))));
  }

  /// Navigates to the orderDetailsScreen when the action is triggered.
  ///
  /// The [BuildContext] parameter is used to build the navigation stack.
  /// When the action is triggered, this function uses the [Navigator] widget
  /// to push the named route for the orderDetailsScreen.
  onTapOrder(BuildContext context) {
    Navigator.pushNamed(context, AppRoutes.orderDetailsScreen);
  }

  /// Navigates back to the previous screen.
  ///
  /// This function takes a [BuildContext] object as a parameter, which is used
  /// to navigate back to the previous screen.
  onTapArrowleftone(BuildContext context) {
    Navigator.pop(context);
  }
}
